(function(PiewPiew){
  
  // LOCAL VARS ////////////////////////////////////////////////////////////////

  // HELPER FUNCTIONS //////////////////////////////////////////////////////////

  // MODULE DEFS ///////////////////////////////////////////////////////////////

  PiewPiew.Data = PiewPiew.Data || {};

  // CLASS DEFS ////////////////////////////////////////////////////////////////

  PiewPiew.Data.Model = PiewPiew.Class(PiewPiew.Base, {
  
    // PUBLIC PROPERTIES ///////////////////////////////////////////////////////
  
    // GETTERS AND SETTERS /////////////////////////////////////////////////////  
  
    // PUBLIC METHODS //////////////////////////////////////////////////////////
    
    handleChanges: function(changes){
      this.trigger(PiewPiew.Data.Model.events.CHANGE, changes);
      return this;
    }

    // PRIVATE METHODS /////////////////////////////////////////////////////////
  
  });

  PiewPiew.Data.Model.events = {
    CHANGE: "PiewPiew.Model.events.CHANGE", 
  }
})(PiewPiew);